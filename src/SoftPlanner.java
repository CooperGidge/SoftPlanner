    private void changePage(IndexInt i1, IndexInt i2, IndexInt i3, boolean isNext, List list) {
        if (!isNext) {
            if (i1.value() > 0) {
                i1.subtractThree();
                i2.subtractThree();
                i3.subtractThree();
                load.doClick();
            } else {
                JOptionPane.showMessageDialog(null, "You are already at the start of the list.");
            }
        } else {
            if (i3.value() < (list.getSize()-1)) {
                i1.addThree();
                i2.addThree();
                i3.addThree();
                load.doClick();
            } else {
                JOptionPane.showMessageDialog(null, "You are already at the end of the list.");
            }
        }
    }
    
